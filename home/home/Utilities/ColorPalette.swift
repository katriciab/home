import Foundation

class ColorPalette {
    class func coolGray() -> UIColor {
        return UIColor(hex:0xE4E3E1)
    }
    
    class func tungsten() -> UIColor {
        return UIColor(hex:0xFFD6AA);
    }
    
    class func candle() -> UIColor {
        return UIColor(hex:0xF0A91A);
    }
}